package it.edenred.bean;

import org.apache.struts.action.ActionForm;

public class Affiliato extends ActionForm {

	private static final long serialVersionUID = -8906685717119882249L;
	
	private String codAffiliato;
	private String indirizzoLocale;
	private String nomeLocale;
	private String telefono;
	private String localita;
	
	public String getCodAffiliato() {
		return codAffiliato;
	}
	
	public void setCodAffiliato(String codAffiliato) {
		this.codAffiliato = codAffiliato;
	}
	
	public String getIndirizzoLocale() {
		return indirizzoLocale;
	}
	
	public void setIndirizzoLocale(String indirizzoLocale) {
		this.indirizzoLocale = indirizzoLocale;
	}
	
	public String getNomeLocale() {
		return nomeLocale;
	}
	
	public void setNomeLocale(String nomeLocale) {
		this.nomeLocale = nomeLocale;
	}
	
	public String getTelefono() {
		return telefono;
	}
	
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}

	public String getLocalita() {
		return localita;
	}

	public void setLocalita(String localita) {
		this.localita = localita;
	}
}
